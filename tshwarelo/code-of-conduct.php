        <h2>Our Commitment</h2>
        <p>We are dedicated to maintaining a safe, respectful, and supportive environment for children, parents, and staff.</p>
        
                <h3>For Staff and Caregivers</h3>
        <ul>
            <li>**NO Physical Punishment:** Corporal punishment (hitting, slapping) or any form of verbal/emotional abuse (shaming, humiliation) is strictly forbidden.</li>
            <li>**Mandatory Reporting:** Report any suspicion of abuse or neglect immediately to the Head/Principal and the relevant Social Worker.</li>
            <li>**Supervision:** Maintain strict child-to-staff ratios and supervise children at all times (play, naps, meals).</li>
        </ul>

        <h3>Expectations for Parents & Guardians</h3>
        <ul>
            <li>**Absence/Sickness:** Inform the creche immediately if the child is absent or sick. Do not send a child with contagious illness (e.g., fever) to the creche.</li>
            <li>**Collection:** Inform staff if someone different (and authorized) will be collecting your child.</li>
            <li>**Respect:** Maintain courteous and respectful behaviour towards all staff members on the premises.</li>
        </ul>

        <h3>Expectations for Children</h3>
        <ul>
            <li>**Respect Others:** Treat all friends and teachers with kindness and use polite words.</li>
            <li>**Keep Hands to Self:** Use gentle hands; hitting, pushing, biting, or kicking is not allowed.</li>
            <li>**Listen and Follow:** Pay attention to teachers and follow instructions quickly and safely.</li>
            <li>**Share and Tidy:** Share toys, take turns, and help put materials away when finished playing.</li>
        </ul>

        <h3>Professionalism & Culture</h3>
        <ul>
            <li>**Equal Respect:** Treat all children equally, regardless of gender, culture, or background.</li>
            <li>**Respect Local Values:** Engage positively with parents and community elders, and respect the customs and mother tongues of the village.</li>
            <li>**No Impairing Substances:** Staff must never consume alcohol or illegal drugs on the premises or while on duty.</li>
            <li>**Confidentiality:** Keep all information regarding a child's family and personal circumstances strictly confidential.</li>
        </ul>
